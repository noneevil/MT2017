
namespace CommonUtils.Enumeration
{
    /// <summary>
    /// 文件或者磁盘容量大小
    /// </summary>
    public enum DiskSizeUnit
    {
        /// <summary>
        /// K = 1024字节
        /// </summary>
        KB = 1024,
        /// <summary>
        /// M = 1024 * 1024字节
        /// </summary>
        MB = 1024 * 1024,
        /// <summary>
        /// G = 1024 * 1024 * 1024字节
        /// </summary>
        GB = 1024 * 1024 * 1024
    }
}
